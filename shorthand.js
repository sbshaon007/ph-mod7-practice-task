let score = 50;

score += 10;
console.log('After += 10: ' , score);

score -= 5;
console.log('After -= 5: ', score);

score *= 2;
console.log('After *= 2:' , score);

score /= 5;
console.log('After /= 5: ' , score);

score %= 4;
console.log('After %= 4: ' , score);


// Expected Console Output

// After += 10: 60

// After -= 5: 55

// After *= 2: 110

// After /= 5: 22

// After %= 4: 2